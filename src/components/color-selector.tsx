function ColorButton({
  color,
  onClick,
  current,
}: {
  color: NamedColor;
  onClick: () => void;
  current: boolean;
}) {
  return (
    <button
      className={cn(
        "w-6 h-6 rounded-md hover:scale-105 transition-all cursor-pointer active:w-5",
        current && "w-10 active:w-11"
      )}
      style={{ backgroundColor: color }}
      onClick={onClick}
    />
  );
}

export function ColorSelector({
  currentColor,
  setColor,
}: {
  currentColor: NamedColor;
  setColor: (color: NamedColor) => void;
}) {
  const { theme } = useTheme();
  const isDark = theme === "dark";
  const colors: NamedColor[] = [
    "wheat",
    "white",
    "ghostwhite",
    "green",
    "blue",
    "purple",
  ];
  const darkColors: NamedColor[] = [
    "darkgrey",
    "dimgrey",
    "black",
    "green",
    "blue",
    "purple",
  ];
  const chosenColors = isDark ? colors : darkColors;
  return (
    <div className="flex gap-2 items-center">
      {chosenColors.map((color) => (
        <ColorButton
          key={color}
          color={color}
          onClick={() => setColor(color)}
          current={currentColor === color}
        />
      ))}
    </div>
  );
}
